function Start()
    CreateWindow(SCREEN_WIDTH, SCREEN_HEIGHT)
    SetWindowTitle("Simple Tile Map")
    
    waterImage = LoadImage("images/water.png")
    grassImage = LoadImage("images/grass.png")
    wallImage = LoadImage("images/wall.png")
end

function Update()
end

function Draw()
    for i=1,numTiles do
        local image = nil
                               
        if tiles[i] == GRASS then image = grassImage
        elseif tiles[i] == WALL then image = wallImage
        else image = waterImage end
                
        DrawImage(image, (i - 1) % TILES_PER_ROW * TILE_SIZE, math.floor((i - 1) / TILES_PER_ROW) * TILE_SIZE)
    end 
end
